package com.webook.domain;

public class CouponVO {
	private String coupon_no;
	private String user_email;
	private String order_no;
	private String coupon_info_id;
	private String coupon_desc;
	private int coupon_discount_price;
	private String coupon_date;
	
	public String getCoupon_no() {
		return coupon_no;
	}
	public void setCoupon_no(String coupon_no) {
		this.coupon_no = coupon_no;
	}
	public String getUser_email() {
		return user_email;
	}
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	public String getOrder_no() {
		return order_no;
	}
	public void setOrder_no(String order_no) {
		this.order_no = order_no;
	}
	public String getCoupon_info_id() {
		return coupon_info_id;
	}
	public void setCoupon_info_id(String coupon_info_id) {
		this.coupon_info_id = coupon_info_id;
	}
	public String getCoupon_desc() {
		return coupon_desc;
	}
	public void setCoupon_desc(String coupon_desc) {
		this.coupon_desc = coupon_desc;
	}
	public int getCoupon_discount_price() {
		return coupon_discount_price;
	}
	public void setCoupon_discount_price(int coupon_discount_price) {
		this.coupon_discount_price = coupon_discount_price;
	}
	public String getCoupon_date() {
		return coupon_date;
	}
	public void setCoupon_date(String coupon_date) {
		this.coupon_date = coupon_date;
	}
	
	
	
	
}
